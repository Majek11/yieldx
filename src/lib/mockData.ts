type Timeframe = '1W' | '1M' | '3M' | '6M' | '1Y' | 'ALL';

export const getChartData = (timeframe: Timeframe) => {
    const slices: Record<Timeframe, number> = {
        '1W': 2,
        '1M': 5,
        '3M': 13,
        '6M': 26,
        '1Y': 52,
        'ALL': portfolioHistory.length,
    };
    const count = slices[timeframe];
    return portfolioHistory.slice(Math.max(0, portfolioHistory.length - count));
};

